CREATE TABLE car_sales (
    Car_id text,
	Date date,
	CustomerName varchar(50),
	Gender varchar(20),
	AnnualIncome int,
	Dealer_Name text,
	Company VARCHAR(100),
    Model VARCHAR(100),
	Engine text,
	Transmission varchar(20),
	Color varchar (30),
	Price int,
	Dealer_No text,
	BodyStyle varchar (30),
	Phone text,
	Dealer_Region text
);
select * from car_sales 
